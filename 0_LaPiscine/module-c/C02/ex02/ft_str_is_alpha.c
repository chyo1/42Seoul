int	ft_str_is_alpha(char *str)
{
	unsigned int		i;
	char				c;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if (!(('a' <= c && c <= 'z') || ('A' <= c && c <= 'Z')))
			return (0);
		i++;
	}
	return (1);
}
