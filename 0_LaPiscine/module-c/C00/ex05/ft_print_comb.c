#include <unistd.h>

void	init_a(char *a)
{
	a[0] = '0';
	a[1] = '1';
	a[2] = '2';
	a[3] = ',';
	a[4] = ' ';
}

void	print(char *a, char c)
{
	if (c != '7')
		write(1, a, 5);
	else
		write(1, a, 3);
}

void	ft_print_comb(void)
{
	char	a[5];

	init_a(a);
	while (a[0] <= '7')
	{
		a[1] = '1';
		while (a[1] <= '8')
		{
			a[2] = '2';
			while (a[2] <= '9')
			{
				if (a[0] < a[1] && a[1] < a[2])
					print(a, a[0]);
				a[2]++;
			}
			a[1]++;
		}
		a[0]++;
	}
}
