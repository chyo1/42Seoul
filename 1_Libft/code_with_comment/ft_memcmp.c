#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*a;
	unsigned char	*b;
	size_t			i;

	// man page에 unsigned char로 비교
	/*
	why? memory가 8비트씩 비교연산을 하는데 signed로 비교할 경우
	127 ~의 수가 c에 들어왔을 때 비교할 수 없기 때문
	*/
	a = (unsigned char *)s1;
	b = (unsigned char *)s2;
	i = 0;
	while (i < n)
	{
		if (a[i] != b[i])
			return (a[i] - b[i]);
		i++;
	}
	return (0);
}
