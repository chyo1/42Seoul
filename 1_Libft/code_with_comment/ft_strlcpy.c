#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t detsize)
{
	size_t	i;
	size_t	src_len;

	i = 0;
	src_len = ft_strlen(src);
	if (detsize != 0)
	{
		while (i + 1 < detsize && src[i] != '\0')
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (src_len);
}
